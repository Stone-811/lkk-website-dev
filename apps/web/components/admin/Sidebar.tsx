export default function Sidebar({ user }: { user: UserSession }) {
  const pathname = usePathname();
  const [pendingLeadsCount, setPendingLeadsCount] = useState(0);

  // 取得待處理預約數量（只計算 booking 類型）
  useEffect(() => {
    async function fetchPendingLeads() {
      try {
        const res = await fetch('/api/admin/leads?type=booking');
        if (res.ok) {
          const data = await res.json();
          const pendingCount = (data.data || []).filter(
            (lead: any) => lead.status === 'new'
          ).length;
          setPendingLeadsCount(pendingCount);
        }
      } catch (error) {
        console.error('Failed to fetch pending leads:', error);
      }
    }
    fetchPendingLeads();
  }, []);

  const filteredNav = navigation.filter(
    (item) => !item.adminOnly || user.role === 'admin'
  );

  return (
    <aside className="w-64 bg-gray-900 text-white flex flex-col">
      {/* Logo */}
      <div className="h-16 flex items-center px-6 border-b border-gray-800">
        <Link href="/admin/dashboard" className="text-xl font-bold text-orange">
          練健康 CMS
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 py-4 overflow-y-auto">
        <ul className="space-y-1 px-3">
          {filteredNav.map((item) => {
            const isActive = pathname === item.href || pathname.startsWith(item.href + '/');

            return (
              <li key={item.name}>
                <Link
                  href={item.href}
                  className={`flex items-center gap-3 px-3 py-2 rounded-lg transition-colors ${
                    isActive
                      ? 'bg-orange text-white'
                      : 'text-gray-400 hover:bg-gray-800 hover:text-white'
                  }`}
                >
                  {item.icon}
                  <span>{item.name}</span>
                  {item.badge && pendingLeadsCount > 0 && (
                    <span className="ml-auto bg-orange text-white text-xs px-2 py-0.5 rounded-full">
                      {pendingLeadsCount}
                    </span>
                  )}
                </Link>
              </li>
            );
          })}
        </ul>
      </nav>

      {/* Back to site */}
      <div className="px-3 py-2">
        <Link
          href="/"
          className="flex items-center gap-2 px-3 py-2 text-gray-400 hover:text-white transition-colors"
        >
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 19l-7-7m0 0l7-7m-7 7h18" />
          </svg>
          返回官網
        </Link>
      </div>

      {/* User Info */}
      <div className="p-4 border-t border-gray-800">
        <div className="flex items-center gap-3">
          <div className="w-10 h-10 bg-orange rounded-full flex items-center justify-center text-white font-medium">
            {user.name.charAt(0)}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium truncate">{user.name}</p>
            <p className="text-xs text-gray-400 truncate">{user.email}</p>
          </div>
        </div>
      </div>
    </aside>
  );
}
